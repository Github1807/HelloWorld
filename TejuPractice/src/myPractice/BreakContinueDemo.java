package myPractice;

public class BreakContinueDemo {

		public static void main(String args[])
	    {
	        for (int i = 0; i < 10; i++) {
	            // If the number is 2
	            // skip and continue
	            if (i == 6)
	                continue;
	 
	            System.out.print(i + " ");
	        }
	    

	 


    for (int i = 0; i < 10; i++) {
        // If the number is 2
        // skip and continue
        if (i == 6)
            break;

        System.out.print(i + " ");
    }
}}
