package myPractice;

public class DigitPatternIncrementDigitWise {
	public static void main(String args[]) {
	    int i,j;
	    int value=0;
	    for(i=1;i<=5;i++) {
	    	for(j=1;j<=i;j++) {
	    		value=value+1;
	    		System.out.print(value);
	    	}
	    	System.out.println(" ");
	    }
	    
}
	
}
